package com.example.hernan.tripmoney;

//**************************************************************************************************
// Clase que contiene los datos con el formato para el arraylist
//**************************************************************************************************
public class DatosListViewDescripcion
{
    private int Id;
    private String Nombre;
    private String Descripcion;
    private float Afavor;

    //**********************************************************************************************
    // Constructor de la clase
    //**********************************************************************************************
    public DatosListViewDescripcion(String name,String desc, float gastado)
    {
    //    Id = id;
        Nombre = name;
        Descripcion = desc;
        Afavor = gastado;
    }

    //**********************************************************************************************
    // Obtengo el id
    //**********************************************************************************************
    public int getId()
    {
        return Id;
    }

    //**********************************************************************************************
    // Obtengo el nombre
    //**********************************************************************************************
    public String getNombre()
    {
        return Nombre;
    }

    //**********************************************************************************************
    // Obtengo la descripcion
    //**********************************************************************************************
    public String getDescripcion()
    {
        return Descripcion;
    }

    //**********************************************************************************************
    // Obtengo el monto a favor
    //**********************************************************************************************
    public float getAFavor()
    {
        return Afavor;
    }

}
