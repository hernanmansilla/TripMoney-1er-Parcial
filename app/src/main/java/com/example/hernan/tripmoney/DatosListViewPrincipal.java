package com.example.hernan.tripmoney;

//**************************************************************************************************
// Clase que contiene los datos con el formato para el arraylist
//**************************************************************************************************
public class DatosListViewPrincipal
{
    private int Id;
    private String Nombre;
    private float Debe;
    private float Afavor;
    private int Imagen_titulo;

    //**********************************************************************************************
    // Constructor de la clase
    //**********************************************************************************************
    public DatosListViewPrincipal(String name, float debe, float afavor,int imag)
    {
      //  Id=id;
        Nombre = name;
        Debe = debe;
        Afavor = afavor;
        Imagen_titulo = imag;
    }

    //**********************************************************************************************
    // Obtengo el id
    //**********************************************************************************************
    public int getId()
    {
        return Id;
    }

    //**********************************************************************************************
    // Set el id
    //**********************************************************************************************
    public void setId(int id)
    {
        Id=id;
    }

    //**********************************************************************************************
    // Obtengo el nombre
    //**********************************************************************************************
    public String getNombre()
    {
        return Nombre;
    }

    //**********************************************************************************************
    // Obtengo lo que debe
    //**********************************************************************************************
    public float getDebe()
    {
        return Debe;
    }

    //**********************************************************************************************
    // Obtengo lo que posee a favor
    //**********************************************************************************************
    public float getAfavor()
    {
        return Afavor;
    }

    //**********************************************************************************************
    // Obtengo una imagen
    //**********************************************************************************************
    public int getImagen()
    {
        return Imagen_titulo;
    }

}

